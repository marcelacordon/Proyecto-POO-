public class user {

    String userr;
    String password;

    user (String us, String pass) {
        this.userr = us;
        this.password = pass;
    }

    user(){
    }
    


    public String getUserr() {
        return userr;
    }
    public void setUserr(String userr) {
        this.userr = userr;
    }
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }

    


    
}
